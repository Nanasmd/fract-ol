#include "libft.h"

int	ft_min(int v1, int v2)
{
	if (v1 > v2)
		return (v1);
	return (v2);
}

int	ft_abs(int v1)
{
	if (v1 < 0)
		return (v1 * -1);
	return (v1);
}
